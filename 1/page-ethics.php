            <div class="txt_content">
                <p class="indent">
                    <br>1.ผู้ประเมินบทความต้องไม่เปิดเผยข้อมูลเกี่ยวกับต้นฉบับที่ส่งมาเพื่อการประเมินแก่บุคคลอื่นนอกเหนือจากผู้นิพนธ์และผู้ประเมิน
                    <br>2.ผู้ประเมินบทความควรแจ้งและปฏิเสธการประเมินกับบรรณาธิการหากมีผลประโยชน์ทับซ้อนกับบทความที่ส่งมาให้พิจารณา
                    <br>3.ผู้ประเมินบทความควรประเมินบทความในสาขาที่ตนเชี่ยวชาญโดยพิจารณาความสำคัญของเนื้อหา คุณภาพการวิเคราะห์และความเข้มข้นของผลงาน ไม่ควรใช้ความเห็นส่วนตัวที่ไม่มีข้อมูลรองรับมาเป็นเกณฑ์ในการตัดสินใจ
                    <br>4.ผู้ประเมินบทความต้องระบุผลงานวิจัยที่สำคัญและสอดคล้องกับบทความที่กำลังประเมินแต่ผู้นิพนธ์ไม่ได้อ้างถึงด้วยหากมีความซ้ำซ้อนกับผลงานชิ้นอื่นต้องแจ้งให้บรรณาธิการทราบด้วย
                    <br>5.ผู้ประเมินควรตรวจสอบและส่งบทวิจารณ์ภายในระยะเวลาที่คณะกรรมการบรรณาธิการเห็นชอบ หากไม่สามารถตรวจสอบต้นฉบับได้ภายในระยะเวลาที่กำหนดผู้ประเมินจะต้องแจ้งให้กองบรรณาธิการทราบทันที
                </p>
