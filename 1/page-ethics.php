            <div class="txt_head1">
                <br>บทบาทหน้าที่และความรับผิดชอบของบรรณาธิการ (Duties of Editors)
            </div>
            <div class="txt_content">
                <p class="indent">
                    <br>1.บรรณาธิการวารสารมีหน้าที่พิจารณาคุณภาพของบทความเพื่อตีพิมพ์ในวารสารโดยคัดกรองตรวจสอบ บทความที่ส่งมาเพื่อตีพิมพ์ด้วยหลักการทางวิชาการ
                    <br>2.บรรณาธิการต้องไม่เปิดเผยข้อมูลของผู้เขียนและผู้ประเมินบทความในระหว่างการประเมินบทความ
                    <br>3.บรรณาธิการควรดำเนินการทุกอย่างเพื่อให้มั่นใจในคุณภาพของบทความที่ตีพิมพ์เพื่อรับรองคุณภาพของงานวิจัยที่ตีพิมพ์ และตระหนักว่าวารสาร และแต่ละส่วนของวารสาร มีวัตถุประสงค์ และมาตรฐานที่ต่างกัน
                    <br>4.การตัดสินใจของบรรณาธิการต่อการยอมรับหรือปฏิเสธบทความวิจัยเพื่อการตีพิมพ์ ควรขึ้นอยู่กับความถูกต้องตามหลักวิชาการ ความสำคัญ ความใหม่ และความชัดเจนของบทความวิจัย ตลอดจนความเกี่ยวข้องกับขอบเขตของวารสาร
                    <br>5.บรรณาธิการควรมีการชี้แจงหรือให้ข้อมูลเกี่ยวกับกระบวนการตรวจสอบประเมินบทความ(peer review) นอกจากนี้ควรมีความพร้อมในการชี้แจงความเบี่ยงเบนต่าง ๆ จากกระบวนการตรวจสอบที่ได้ระบุไว้
                    <br>6.บรรณาธิการควรจัดพิมพ์คำแนะนำแก่ผู้นิพนธ์ในทุกประเด็นที่บรรณาธิการคาดหวัง ในทุกเรื่องที่ผู้นิพนธ์ควรรับทราบ และควรมีการปรับปรุงคำแนะนำให้ทันสมัยอยู่เสมอ พร้อมทั้งควรมีการอ้างอิงหรือการเชื่อมโยงกับระเบียบดังกล่าวนี้ด้วย
                    <br>7.บรรณาธิการไม่ควรเปลี่ยนแปลงการตัดสินใจในการตอบรับบทความที่ถูกปฏิเสธการติพิมพ์ไปแล้ว ยกเว้นมีปัญหาร้ายแรงเกิดขึ้นในระหว่างการส่งบทความมารับการพิจารณา
                    <br>8.บรรณาธิการใหม่ไม่กลับคำตัดสินใจมาตีพิมพ์บทความที่บรรณาธิการคนก่อนตอบปฏิเสธไปแล้ว ยกเว้นมีการพิสูจน์ถึงปัญหาร้ายแรงที่เกิดขึ้น
                    <br>9.บรรณาธิการพิจารณาผู้ทรงคุณวุฒิที่มีความเชี่ยวชาญตรงตามสาขาของบทความและส่งบทความให้ผู้พิจารณาบทความโดยผู้พิจารณาบทความจะไม่ทราบชื่อผู้แต่งและผู้นิพนธ์จะไม่ทราบชื่อผู้พิจารณาบทความ (Double blind)
                    <br>10.บรรณาธิการต้องไม่มีผลประโยชน์ทับซ้อนกับผู้นิพนธ์ผู้ประเมิน และทีมผู้บริหาร
                    <br>11.บรรณาธิการต้องมีการตรวจบทความในด้านการคัดลอกงานผู้อื่น (Plagiarism) หากพบต้องหยุดกระบวนการประเมินและติดต่อผู้นิพนธ์ทันทีเพื่อขอคำชี้แจง เพื่อประกอบการ “ตอบรับ” หรือ “ปฏิเสธ” การตีพิมพ์บทความนั้น
                </p>
