            <div class="txt_content">
                <br>&emsp;&emsp;จริยธรรมในการตีพิมพ์ผลงานวิจัยในวารสารวิชาการ (Publication Ethics) วารสารสำนักงานสาธารณสุขจังหวัดขอนแก่น เป็นสื่อกลางในการเผยแพร่งานวิจัยหรือบทความวิชาการระหว่างนักวิจัยหรือผู้นิพนธ์กับสังคมภายนอก ดังนั้นเพื่อให้การสื่อสารทางวิชาการเป็นไปอย่างถูกต้อง มีคุณภาพ โปร่งใสและสอดคล้องกับมาตรฐานการตีพิมพ์ระดับนานาชาติ จึงได้กำหนดแนวทาง วิธีปฏิบัติที่ดี และจริยธรรมของการตีพิมพ์เผยแพร่ผลงานวิจัยหรือผลงานวิชากาตามแนวทางของหน่วยงาน Committee on Publication Ethics (COPE) ไว้ดังนี้
            </div>

            <div class="txt_head1">
                <br>บทบาทหน้าที่ของผู้นิพนธ์ (Duties of Authors)
            </div>
            <div class="txt_content">
                <p class="indent">
                    <br>1.ผู้นิพนธ์เมื่อส่งบทความไปยังบรรณาธิการ บทความนี้ต้องไม่เคยเผยแพร่มาก่อนและไม่ได้รับการพิจารณาให้ตีพิมพ์ที่อื่นมาก่อน
                    <br>2.ผู้นิพนธ์ควรแจ้งเตือนบรรณาธิการโดยทันทีหากพบข้อผิดพลาดที่สำคัญหรือข้อมูลที่ไม่ถูกต้องในบทความที่เผยแพร่เพื่อถอนการตีพิมพ์หรือแก้ไขข้อผิดพลาดในสิ่งพิมพ์นี้ หากบรรณาธิการรู้จากบุคคลที่สามว่ามีข้อผิดพลาดที่สำคัญในงานที่ตีพิมพ์และแจ้งให้ผู้นิพนธ์ทราบถึงข้อผิดพลาดดังกล่าว ข้อผิดพลาดจะต้องได้รับการแก้ไขโดยทันทีและผู้นิพนธ์มีหน้าที่ต้องแสดงหลักฐานความถูกต้องของข้อมูลให้กองบรรณาธิการทราบ
                    <br>3.ผู้นิพนธ์ต้องรับผิดชอบต่อเนื้อหาของสิ่งพิมพ์ของตน หากผู้นิพนธ์ใช้เนื้อหาที่นำมาจากนักวิจัยคนอื่นโดยตรง ต้องมีการอ้างอิงหรือยกมาอย่างเหมาะสม
                    <br>4.ผู้นิพนธ์ต้องเขียนบทความวิจัยให้ถูกต้องตามรูปแบบที่กำหนดไว้ใน “คำแนะนำในการส่งต้นฉบับ”
                    <br>5.ผู้นิพนธ์ที่มีชื่อปรากฏในบทความทุกคน ต้องเป็นผู้ที่มีส่วนในการดำเนินการวิจัยจริง
                    <br>6.ผู้นิพนธ์จะต้องรับผิดชอบในการเปิดเผยแหล่งที่มาของการสนับสนุนทุนทั้งหมด สำหรับโครงการวิจัย โดยอธิบายไว้ในต้นฉบับที่ส่งมา รวมทั้งระบุผู้นิพนธ์ร่วมทั้งหมดที่อำนวยความสะดวกในการวิจัย
                    <br>7.ผู้นิพนธ์ต้องระบุผลประโยชน์ทับซ้อน (หากมี)
                    <br>8.ผู้นิพนธ์ต้องรับผิดชอบต่อเนื้อหาของสิ่งพิมพ์ของตน หากผู้นิพนธ์ใช้เนื้อหาที่นำมาจากนักวิจัยคนอื่นโดยตรง ต้องมีการอ้างอิงหรือยกมาอย่างเหมาะสม
                </p>
